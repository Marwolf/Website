                        <h4><b>Frequently Asked Questions</b></h4><br />
                        
                        <p class="question">Do I have to subscribe, donate, or buy anything?</p>
                        <p class="answer">No! Open RSC is forever a free game. Server costs are super cheap and we have regular jobs.<br /> 
                        This is our hobby and we don't need any money. Just come have fun!</p><br />
                        
                        <p class="question">Who are the Open RSC admins?</p>
                        <p class="answer">Marwolf and Kenix</p><br />
                        
                        <p class="question">How does the Open RSC government run the game?</p>
                        <p class="answer">We believe in non-interference. If real life illegal activity is found happening, then we will ban the person.<br /><br />
                            <i>- Swearing, advertising, botting, scamming, hacking, and bullying may be given temporary or <br />
                                permanent mutes if bad behavior persists.</i></p><br />
                        
                        <p class="question">What is Open RSC's stance on botting?</p>
                        <p class="answer">We don't approve but know it is futile to play cat and mouse. We may create another game called<br />
                            something like BotScape Classic where only the best bot scripts can defeat the other bots.</p><br />
                        
                        <p class="question">Does Open RSC need any mods?</p>
                        <p class="answer">Not at the moment. We generally want to leave the in-game players alone.</p><br />
                        
                        <p class="question">How can I help?</p>
                        <p class="answer">Join the Discord chat and come talk to us! We have many issues created in the GitHub repos.</p><br />
                        
                        <p class="question">How do I run my own copy?</p>
                        <p class="answer">Visit the <a href="https://github.com/Marwolf/Open-RSC-Docker">Open-RSC-Docker</a> repo and follow the instructions. Windows / Mac / Linux compatible!</p><br />
                        
                        <p class="question">From what period is Open RSC based on traditional RSC?</p>
                        <p class="answer">We believe the final version of traditional RSC is best to begin with but choose to disable some features<br />
                              added over time that we consider either annoying or pointless. <br /><br />
                              For those that want to run their own version of Open RSC, disabled features can be enabled in the config.
                              </p><br />
                        
