								<span class="d-block">All that is left to do now is kill Elvarg. Make sure to pick up an Anti dragon breath Shield from the Duke of Lumbridge, found on the upper floor of the castle in Lumbridge. You should bring the best armour and one-handed weapon you can use, along with the anti dragon breath shield, though you may bring another shield as well as you will be near aggressive mid-to-high level monsters. Any extra spaces in your inventory should be filled with lots of food and any potions or runes you would like to use. Some players choose to cast spells with magic while fighting with melee. Teleport runes are recommended if you want to avoid walking past skeletons and several Lesser Demons after the fight.</span>
								<span class="pt-3 d-block">Make sure you have the map with you and then head to the Lumbridge Lady and board it.</span>
								<span class="pt-3 d-block">Speak to Ned who will take you to Crandor, though doing so will damage the ship in the process, leaving you stranded on Crandor.</span>
								<img class="pt-2 pb-2 img-fluid" src="../img/quests/440px-Arriving_at_Crandor.png">
								<span class="pt-3 d-block">Once you arrive, leave the ship and make your way around the mountain to the peak, dodging aggressive Skeletons (level 31), Scorpions (level 26), Hobgoblins (level 32), and a singular Lesser Demon (level 79). Once you are at the top, go down the stairs to enter the cave under Crandor.</span>
								<span class="pt-3 d-block">Once you are in the cave, you will be in a room with aggressive level 31 Skeletons. Continue past them and you will see level 54 Skeletons protecting Elvarg's lair. Make sure your anti dragon breath shield is equipped and open the door leading to Elvarg's lair. Be prepared for her to immediately start attacking you.</span>
								<img class="pt-2 pb-2 img-fluid" src="../img/quests/440px-Fighting_Elvarg.png">
								<span class="pt-3 d-block">Attack Elvarg (level 110) and keep an eye on your HP. The dragon breath attack will hit you constantly for small numbers and is unavoidable. Once your Hits drop to about half, run away and eat 1 or 2 pieces of food before resuming combat.</span>
								<span class="pt-3 d-block">Once Elvarg is defeated, the quest is complete and you will be teleported back out in to the middle of the level 54 skeletons. If you are weakened from the fight it may be a good idea to teleport, otherwise, head past the skeletons and lesser demons and use the secret passage to exit to Karamja.</span>
								<span class="pt-3 d-block">You can now return to Oziach and purchase a Rune Plate Mail Body for 65,000 coins.</span>
