								<span class="pt-3 d-block">Once inside, you must kill the Rats (level 13) until you find one that drops a Red key. Use it on the north-west door and head up the ladder. If you can't find the key, make sure you check the drops carefully and you're not killing the same rat over and over.</span>
								<span class="pt-3 d-block">Now kill the Ghosts (level 25) until you find the one that drops the Orange Key. Go to the east wall and use it on the second door from the north and climb the ladder.</span>
								<span class="pt-3 d-block">Kill the Skeletons (level 31) on this floor until you get a yellow key and use it on the south-western door. Follow the passageway and then go down a series of ladders to reach the basement.</span>
								<span class="pt-3 d-block">Kill the Zombies (level 32) until you get a Blue key and use it to unlock the northern-most door with Melzar the Mad on the other side. Note that southern door is an emergency exit that will take you outside, requiring you to complete the maze again if you leave. Kill Melzar the Mad (level 45) and use the Magenta key he drops on the north door, leading to the Lesser Demon.</span>
								<span class="pt-3 d-block">Note again that the western door here leads to the emergency exit. Kill the lesser demon (level 79) and use the black key it drops on the northern door leading to a small room with the wooden floors. Search the chest in this room to obtain the first map piece. When you have it, climb the ladder to exit the maze.</span>
